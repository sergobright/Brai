WHERE i.item_roles_id IS NULL
  AND r.items_id = i.id
  AND r.item_role_types_id = 2;

UPDATE focus_sessions f
SET item_roles_id = r.id
FROM item_roles r
WHERE f.item_roles_id IS NULL
  AND r.items_id = f.id
  AND r.item_role_types_id = 3;

UPDATE events e
SET item_roles_id = r.id
FROM item_roles r
JOIN item_role_types rt ON rt.id = r.item_role_types_id
WHERE e.item_roles_id IS NULL
  AND r.items_id = e.items_id
  AND rt.title_system = e.subject_type;

UPDATE inbox i
SET initial_event_id = (
  SELECT e.id
  FROM events e
  WHERE e.event_domain = 'inbox'
    AND e.subject_id = i.id
    AND e.event_type = 'create'
  ORDER BY e.occurred_at_utc, e.domain_sequence
  LIMIT 1
)
WHERE i.initial_event_id IS NULL;

