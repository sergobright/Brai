CREATE TABLE IF NOT EXISTS workflow_definitions (
  id text NOT NULL,
  version integer NOT NULL CHECK (version > 0),
  title text NOT NULL,
  description text NOT NULL DEFAULT '',
  status text NOT NULL CHECK (status IN ('active', 'retired')),
  task_queue text NOT NULL,
  steps_json text NOT NULL DEFAULT '[]',
  diagram_mermaid text NOT NULL,
  input_schema_version text NOT NULL,
  input_schema_json text NOT NULL,
  output_schema_version text NOT NULL,
  output_schema_json text NOT NULL,
  created_at_utc text NOT NULL,
  updated_at_utc text NOT NULL,
  PRIMARY KEY (id, version)
);

CREATE TABLE IF NOT EXISTS role_contracts (
  id text PRIMARY KEY,
  item_role_types_id integer NOT NULL UNIQUE REFERENCES item_role_types(id),
  payload_table text NOT NULL,
  link_column text NOT NULL DEFAULT 'item_roles_id',
  lifecycle_json text NOT NULL DEFAULT '{}',
  workflow_definition_id text,
  workflow_definition_version integer,
  input_schema_version text,
  output_schema_version text,
  owner text NOT NULL,
  event_rules_json text NOT NULL DEFAULT '{}',
  created_at_utc text NOT NULL,
  updated_at_utc text NOT NULL,
  FOREIGN KEY (workflow_definition_id, workflow_definition_version)
    REFERENCES workflow_definitions(id, version)
);

CREATE TABLE IF NOT EXISTS workflow_executions (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  workflow_definition_id text NOT NULL,
  workflow_definition_version integer NOT NULL,
  workflow_id text NOT NULL UNIQUE,
  run_id text,
  role_contract_id text NOT NULL REFERENCES role_contracts(id),
  raw_record_id text NOT NULL,
  status text NOT NULL CHECK (status IN ('queued', 'running', 'completed', 'failed', 'needs_review')),
  current_step text NOT NULL,
  attempt_count integer NOT NULL DEFAULT 0 CHECK (attempt_count >= 0),
  last_error text,
  started_at_utc text,
  completed_at_utc text,
  created_at_utc text NOT NULL,
  updated_at_utc text NOT NULL,
  user_id text,
  FOREIGN KEY (workflow_definition_id, workflow_definition_version)
    REFERENCES workflow_definitions(id, version)
);

