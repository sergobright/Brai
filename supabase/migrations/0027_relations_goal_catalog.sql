CREATE UNIQUE INDEX IF NOT EXISTS idx_relation_types_user_key
  ON relation_types (user_id, key)
  WHERE is_system = 0;
CREATE INDEX IF NOT EXISTS idx_relation_types_user_status
  ON relation_types (user_id, status, key);

