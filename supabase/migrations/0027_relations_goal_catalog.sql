CREATE TABLE IF NOT EXISTS relations (
  id text PRIMARY KEY,
  user_id text NOT NULL,
  relation_types_id text NOT NULL REFERENCES relation_types(id) ON DELETE RESTRICT,
  source_items_id text NOT NULL REFERENCES items(id) ON DELETE RESTRICT,
  target_items_id text NOT NULL REFERENCES items(id) ON DELETE RESTRICT,
  status text NOT NULL DEFAULT 'active' CHECK (status IN ('active', 'ended')),
  position integer CHECK (position IS NULL OR position >= 0),
  active_from_utc text NOT NULL,
  active_to_utc text,
  operation_id text NOT NULL,
  ended_operation_id text,
  origin_decision_id text,
  created_by_actor_type text NOT NULL
    CHECK (created_by_actor_type IN ('user', 'agent', 'system')),
  created_by_actor_id text,
  ended_by_actor_type text
    CHECK (ended_by_actor_type IS NULL OR ended_by_actor_type IN ('user', 'agent', 'system')),
  ended_by_actor_id text,
  end_reason text CHECK (end_reason IS NULL OR length(end_reason) <= 500),
  metadata_json text NOT NULL DEFAULT '{}'
    CHECK (length(metadata_json) <= 16000 AND jsonb_typeof(metadata_json::jsonb) = 'object'),
  created_at_utc text NOT NULL,
  updated_at_utc text NOT NULL,
  CHECK (source_items_id <> target_items_id),
  CHECK (
    (status = 'active' AND active_to_utc IS NULL AND ended_operation_id IS NULL AND ended_by_actor_type IS NULL AND end_reason IS NULL)
    OR (status = 'ended' AND active_to_utc IS NOT NULL AND ended_operation_id IS NOT NULL AND ended_by_actor_type IS NOT NULL AND end_reason IS NOT NULL)
  )
);

