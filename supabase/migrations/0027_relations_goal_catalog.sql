CREATE TABLE IF NOT EXISTS relation_types (
  id text PRIMARY KEY,
  user_id text,
  key text NOT NULL,
  title text NOT NULL CHECK (length(title) BETWEEN 1 AND 200),
  description text NOT NULL DEFAULT '' CHECK (length(description) <= 8000),
  directionality text NOT NULL DEFAULT 'symmetric'
    CHECK (directionality IN ('symmetric', 'directed')),
  source_label text NOT NULL DEFAULT '' CHECK (length(source_label) <= 200),
  target_label text NOT NULL DEFAULT '' CHECK (length(target_label) <= 200),
  is_ordered integer NOT NULL DEFAULT 0 CHECK (is_ordered IN (0, 1)),
  status text NOT NULL DEFAULT 'candidate'
    CHECK (status IN ('candidate', 'active', 'retired')),
  is_system integer NOT NULL DEFAULT 0 CHECK (is_system IN (0, 1)),
  created_by_actor_type text NOT NULL
    CHECK (created_by_actor_type IN ('user', 'agent', 'system')),
  created_by_actor_id text,
  created_at_utc text NOT NULL,
  updated_at_utc text NOT NULL,
  retired_at_utc text,
  CHECK (
    (is_system = 1 AND user_id IS NULL)
    OR (is_system = 0 AND user_id IS NOT NULL)
  ),
  CHECK (
    (directionality = 'directed' AND length(btrim(source_label)) > 0 AND length(btrim(target_label)) > 0)
    OR (directionality = 'symmetric' AND source_label = '' AND target_label = '')
  ),
  CHECK (
    (status = 'retired' AND retired_at_utc IS NOT NULL)
    OR (status <> 'retired' AND retired_at_utc IS NULL)
  )
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_relation_types_system_key
  ON relation_types (key)
